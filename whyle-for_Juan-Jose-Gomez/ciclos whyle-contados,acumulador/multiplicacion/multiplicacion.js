let num1 = parseInt(prompt("Ingrese el numero de la tabla de multiplicar"));
let num2 = parseInt(prompt("Ingrese la cantidad por la cual desea ser multiplicado el numero"));

let contador = 1;
let multiplicacion = 0;

while (contador <= num2) {
    
    multiplicacion = multiplicacion + num1
    

    contador ++
}
console.log("El resultado es: ", multiplicacion);